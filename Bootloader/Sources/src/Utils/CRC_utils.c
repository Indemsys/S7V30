/*------------------------------------------------------------------------------
  Вычисление контрольной суммы блока по таблице

 \param b
 \param len
 \param crc - Начальное значение

 \return unsigned short
 ------------------------------------------------------------------------------*/
uint16_t Get_CRC16_of_block(void *b, uint32_t len, uint16_t crc)
{
  uint16_t    q;
  uint8_t     *ptr = (uint8_t*)b;

  while (len--)
  {
    q = *(crc_tbl + (*ptr++ ^  (crc >> 8)));

    crc = (unsigned short)(((q & 0xFF00) ^ (crc << 8)) | (q & 0x00ff));

  }
  return ((unsigned short)(crc ^ 0xFFFF));
}


/*-----------------------------------------------------------------------------------------------------
  two types of crcs are possible: unreflected (bits shift left) and reflected (bits shift right).
  \param buf
  \param len
  \param crc_in
  \param table

  \return uint16_t
-----------------------------------------------------------------------------------------------------*/
static uint16_t crc16_unreflected(uint8_t *buf, uint32_t len, uint16_t crc_in, const uint32_t table[])
{
    /* we use uint32_ts, rather than uint16_ts, as they are likely to be
       faster. We just ignore the top 16 bits and let them do what they want.
    */
  uint32_t crc16 = (uint32_t)crc_in;

  while (len-- != 0) crc16 = table[((crc16 >> 8)^*buf++) & 0xff] ^(crc16 << 8);

  return (uint16_t)crc16;
}

/*-----------------------------------------------------------------------------------------------------


  \param buf
  \param len

  \return uint16_t
-----------------------------------------------------------------------------------------------------*/
uint16_t CRC16_matlab(uint8_t *buf, uint32_t len)
{
  uint16_t crc = crc16_unreflected(buf,len,0,crc16_ccitt_table);
  return (((crc >> 8) & 0xFF) | ((crc << 8) & 0xFF00));
}



